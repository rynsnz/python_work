# define a function without a parameter
def display_message():
    """Display a simple function explanation."""
    print("A function is a code block that returns a value.")

# function call
display_message()

# define a function that requires a parameter to run
def favorite_coffee(drink):
    """Display a simple return statement."""
    print(f"My favorite coffee drink is {drink.title()}.")

# function call with an argument
favorite_coffee('americano')